package com.wl.bharatqr.model;

public class AmexResponseStatus {
private String status_code;
private String status_code_type;
private String new_qr_id;
private String description;
public String getStatus_code() {
	return status_code;
}
public void setStatus_code(String status_code) {
	this.status_code = status_code;
}
public String getStatus_code_type() {
	return status_code_type;
}
public void setStatus_code_type(String status_code_type) {
	this.status_code_type = status_code_type;
}
public String getNew_qr_id() {
	return new_qr_id;
}
public void setNew_qr_id(String new_qr_id) {
	this.new_qr_id = new_qr_id;
}
public String getDescription() {
	return description;
}
public void setDescription(String description) {
	this.description = description;
}


}
